package java_lee_1;

public class ch3_DenyLogicOperatorExample {

	public static void main(String[] args) {
		
		
		boolean play =  true;
		System.out.println(play);
		
		//!는 NOT을 의미
		play = !play; //반대값 대입
		System.out.println(play);
		
		play = !play; // 반대값 대입
		System.out.println(play);
	}

}
